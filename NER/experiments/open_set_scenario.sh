    pipeline/open_set \
    --base en_core_web_trf \
    --tokenizer \
    --ioc \
    --pos ../models/pos.json \
    --lemma ../models/lemma.json \
    --wordnet ../models/synset.csv \
    --cti ../cti \
    --domains enterprise \
    --phrase ../models/phrases.json \
    --matcher subphrase

